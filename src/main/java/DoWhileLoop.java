public class DoWhileLoop {

    public static void main(String[] args) {
        /*
        *  do {
        *    do it;
        *    do it;
        *  } while (<condition>);
        *
        **/

        int i = 0;
        do {
            System.out.println("sayi - " + i);
            i++;
        } while (i < 10);
    }
}
